package modelo;

public class MasaModel extends CantidadModel {
	
	double kilogramos;
	double gramos;
    double toneladas;
    double libras;
    double onzas;
    
	public MasaModel() {
		super();
	}

	public double getKilogramos() {
		return kilogramos;
	}

	public void setKilogramos(double kilogramos) {
		this.kilogramos = kilogramos;
	}

	public double getGramos() {
		return gramos;
	}

	public void setGramos(double gramos) {
		this.gramos = gramos;
	}

	public double getToneladas() {
		return toneladas;
	}

	public void setToneladas(double toneladas) {
		this.toneladas = toneladas;
	}

	public double getLibras() {
		return libras;
	}

	public void setLibras(double libras) {
		this.libras = libras;
	}

	public double getOnzas() {
		return onzas;
	}

	public void setOnzas(double onzas) {
		this.onzas = onzas;
	}
    
    

}
