package modelo;

public class OtrosModel extends CantidadModel{

	int cucharadas;
	int cucharaditas;
	int tazas;
	int tacitas;
	int pintas;
	int mililitros;
	
	public OtrosModel() {
		super();
	}

	public int getCucharadas() {
		return cucharadas;
	}

	public void setCucharadas(int cucharadas) {
		this.cucharadas = cucharadas;
	}

	public int getCucharaditas() {
		return cucharaditas;
	}

	public void setCucharaditas(int cucharaditas) {
		this.cucharaditas = cucharaditas;
	}

	public int getTazas() {
		return tazas;
	}

	public void setTazas(int tazas) {
		this.tazas = tazas;
	}

	public int getTacitas() {
		return tacitas;
	}

	public void setTacitas(int tacitas) {
		this.tacitas = tacitas;
	}

	public int getPintas() {
		return pintas;
	}

	public void setPintas(int pintas) {
		this.pintas = pintas;
	}

	public int getMililitros() {
		return mililitros;
	}

	public void setMililitros(int mililitros) {
		this.mililitros = mililitros;
	}

}
